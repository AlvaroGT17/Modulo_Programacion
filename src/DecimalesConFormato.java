
public class DecimalesConFormato {

	public static void main(String[] args) {
		
		double number1 = 2909834;
		System.out.printf("Primer ejemplo: %,10.4f %n", number1);
		System.out.printf("Segundo ejemplo: %,25.2f %n", number1);
		System.out.printf("Tercer ejemplo: %,10.2f %n", number1);
		System.out.printf("Cuarto ejemplo: %,10.3f %n", number1);

	}

}
